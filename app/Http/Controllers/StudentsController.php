            'year' => Request('year'), 
            'matter' => Request('matter'), 
            'responsible' => Request('responsible'), 
            'address' => Request('address'),
            'phoneNumber' => Request('phoneNumber'),
            'user_id'=>auth()->id()
        ]);
        return redirect()->Route('students_index');
    }

